//condtion statements in javascript
// if statement

let age = 17;
if (age >= 18) {
    console.log("You are eligible to vote.");
}

if (age < 18) {
    console.log("You are not eligible to vote.");
}

// if-else statement
if (age >= 18) {
    console.log("You are eligible to vote.");
} else {
    console.log("You are not eligible to vote.");
}

